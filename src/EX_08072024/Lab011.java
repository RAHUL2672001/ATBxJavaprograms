package EX_08072024;

public class Lab011 {
    public static void main(String[] args) {
         //unary Operator
        int age=+65;       // +,-,*,/ arithmetic operator.
        int car=-1;       // = assignment operator.

        System.out.println(age);
        System.out.println(car);

        //compound Assignment operator

        int a=10;
        // +=,  -=  ,  /=,  %=
        // += , ->

        a+=10; //a+10
        a-=10; //a-10
        a/=10; //a/10
        a*=10; //a*10

        System.out.println(a+=10);
    }
}
